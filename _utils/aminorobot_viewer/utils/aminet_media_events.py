def get_media_event_string_list(event_ids):
    """     docstring
    """
    event_id_ints = [int(x) for x in event_ids.split(',')]

    ret = []

    for event in event_id_ints:
        try:
            ret.append(MEDIA_EVENTS[event])
        except KeyError:
            ret.append('%s: FAIL' % (event))

    return ret
